public class Variables {
    public static void main(String[] args)  {
        
        
        int x;  // declaration
        x = 15;  // assignment
        // or
        int y = 150; // initialization
        byte a = 100;
        short b = 200;
        long z = 65451449648L;   // long variable's values are followed by an L
        float pi = 3.14f;  // float variable's values are followed by an f
        double d = 3.15245668;
        boolean k = true;  // holds either true or false 
        char ch = 'L';  // holds a single character 
        String str = "hello!";  // string is a reference data type in java so it starts with a capital letter


        System.out.println(x);
        System.out.println("and y is : "+y);
        System.out.println("long number is : "+z);
        System.out.println("this is pi : " + pi);
        System.out.println("this is a double variable : " + d);
        System.out.println("is this " + k + " ?");
        System.out.println("this is a character : " + ch);
        System.out.println(str + " world!");
    
    }
}
